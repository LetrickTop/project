import joblib  # Библиотека для загрузки модели

# Загружаем обученную модель (предположим, что модель сохранена как "model.pkl")
model = joblib.load('model.pkl')


def predict_attack(packet_data):
    """
    Функция для предсказания атаки на основе входных данных.
    Здесь предполагается, что модель принимает данные как словарь.
    """
    features = [
        packet_data["src_bytes"],  # Пример: количество байтов от источника
        packet_data["dst_bytes"],  # Пример: количество байтов к назначению
        # Добавьте другие характеристики, которые использует ваша модель
    ]

    prediction = model.predict([features])  # Получаем предсказание
    return "Атака" if prediction == 1 else "Безопасно"